package JavaBasico;

public class Operador {
    public static void main (String args[]){
        double x = 7 * 3;
        double y = 7 / 3;
        double z = 8 % 2;
        double w = 7 * 3 + 7 - 4 / 2;
        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
        System.out.println(w);
    }
}
